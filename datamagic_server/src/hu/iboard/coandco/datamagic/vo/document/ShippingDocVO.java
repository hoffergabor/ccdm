package hu.iboard.coandco.datamagic.vo.document;

public class ShippingDocVO {
	
	private String sorszam;
	private String kelt;
	private String esedkelt;
	private String fizetve;
	private String nev;
	private String netto;
	private String brutto;
	private String allNetto;
	private String allBrutto;
	
	public String getSorszam() {
		return sorszam;
	}
	public void setSorszam(String sorszam) {
		this.sorszam = sorszam;
	}
	public String getKelt() {
		return kelt;
	}
	public void setKelt(String kelt) {
		this.kelt = kelt;
	}
	public String getEsedkelt() {
		return esedkelt;
	}
	public void setEsedkelt(String esedkelt) {
		this.esedkelt = esedkelt;
	}
	public String getFizetve() {
		return fizetve;
	}
	public void setFizetve(String fizetve) {
		this.fizetve = fizetve;
	}
	public String getNev() {
		return nev;
	}
	public void setNev(String nev) {
		this.nev = nev;
	}
	public String getNetto() {
		return netto;
	}
	public void setNetto(String netto) {
		this.netto = netto;
	}
	public String getBrutto() {
		return brutto;
	}
	public void setBrutto(String brutto) {
		this.brutto = brutto;
	}
	public String getAllNetto() {
		return allNetto;
	}
	public void setAllNetto(String allNetto) {
		this.allNetto = allNetto;
	}
	public String getAllBrutto() {
		return allBrutto;
	}
	public void setAllBrutto(String allBrutto) {
		this.allBrutto = allBrutto;
	}

}
