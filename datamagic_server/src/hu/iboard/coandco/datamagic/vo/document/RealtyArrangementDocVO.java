package hu.iboard.coandco.datamagic.vo.document;


public class RealtyArrangementDocVO {
	
	private String sorszam;
	private String lakas;
	private String bejelentve;
	private String bejelento;
	private String hatarido;
	private String befejezve;
	private String netto;
	
	public String getSorszam() {
		return sorszam;
	}
	public void setSorszam(String sorszam) {
		this.sorszam = sorszam;
	}
	public String getLakas() {
		return lakas;
	}
	public void setLakas(String lakas) {
		this.lakas = lakas;
	}
	public String getBejelentve() {
		return bejelentve;
	}
	public void setBejelentve(String bejelentve) {
		this.bejelentve = bejelentve;
	}
	public String getBejelento() {
		return bejelento;
	}
	public void setBejelento(String bejelento) {
		this.bejelento = bejelento;
	}
	public String getHatarido() {
		return hatarido;
	}
	public void setHatarido(String hatarido) {
		this.hatarido = hatarido;
	}
	public String getBefejezve() {
		return befejezve;
	}
	public void setBefejezve(String befejezve) {
		this.befejezve = befejezve;
	}
	public String getNetto() {
		return netto;
	}
	public void setNetto(String netto) {
		this.netto = netto;
	}
	

}
